import numpy as np

a = np.array([1, 2, 3])
b = np.array([4, 5, 6])

addition = a + b
subtraction = a - b
multiplication = a * b
division = a / b

print("Element-wise addition:", addition)
print("Element-wise subtraction:", subtraction)
print("Element-wise multiplication:", multiplication)
print("Element-wise division:", division)
